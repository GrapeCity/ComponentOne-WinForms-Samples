        public static C1Icon GetIcon(string iconName)
        {
            PropertyInfo pi = typeof(IconUtils).GetProperty(iconName, BindingFlags.Public | BindingFlags.Static);
            if (pi != null)
            {
                var icon = pi.GetValue(null, null) as C1Icon;
                if (icon != null)
                {
                    if (icon is C1PathIcon pathIcon)
                    {
                        pathIcon.ViewBox = new Rectangle(0, 0, 800, 800);
                    }
                    var cIcon = new C1CompositeIcon();
                    cIcon.Icons.Add(icon);
                    return cIcon;
                }
            }
            return null;
        }
